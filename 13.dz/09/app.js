// Сначала отфильтруй чётные числа, затем возведи их в квадрат и найди сумму.
// Тест-кейсы:
// [1, 2, 3, 4] → 2² + 4² = 4 + 16 = 20
// [5, 7, 9] → 0
// [0, 10] → 0² + 10² = 0 + 100 = 100

const arr = [5, 7, 9];

const res = arr
  .filter((el) => el % 2 == 0)
  .map((ele) => ele ** 2)
  .reduce((sum, elem) => (sum += elem), 0);
console.log(res);
